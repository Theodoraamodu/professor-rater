package com.example.pa.model;

import jakarta.persistence.Entity;
import jakarta.persistence.Id;

@Entity
public class OverallRating {
    @Id
    private Long id; // Unique identifier for the overall rating
    private Long professorId; // Associated professor's ID
    private Long courseId; // Associated course's ID
    private Long userId; // ID of the user who rated
    private Long collegeId; // Associated college's ID
    private int overallRating; // Overall rating given

    // Getters and setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getProfessorId() {
        return professorId;
    }

    public void setProfessorId(Long professorId) {
        this.professorId = professorId;
    }

    public Long getCourseId() {
        return courseId;
    }

    public void setCourseId(Long courseId) {
        this.courseId = courseId;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public Long getCollegeId() {
        return collegeId;
    }

    public void setCollegeId(Long collegeId) {
        this.collegeId = collegeId;
    }

    public int getOverallRating() {
        return overallRating;
    }

    public void setOverallRating(int overallRating) {
        this.overallRating = overallRating;
    }
}